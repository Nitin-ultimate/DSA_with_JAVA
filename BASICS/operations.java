public class operations {
    public static void main(String[] args) {
        int x = 10, y = 20;;// int data type
        double z = 10.5;// double data type 
        float a = 10.5f;// float data type
        System.out.println(x+y);
        System.out.println(x+y+z);//output is in double
        System.out.println(x-y+a);//output is in float
        System.out.println(x*y);
        System.out.println(x/y);//output is in int
        System.out.println(x%y);
}
}
